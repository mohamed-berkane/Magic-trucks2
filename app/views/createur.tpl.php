            <td>Hirokazu Yasuhara (安 原 広 和, Yasuhara Hirokazu , né le 12 octobre 1965) (également crédité sous le nom de Carol Yas ) est un concepteur de jeux vidéo japonais . Il est surtout connu pour avoir conçu le gameplay et les étapes des premiers jeux vidéo Sonic the Hedgehog pour Sega Genesis dans les années 1990, basés sur des démos techniques et des moteurs programmés par Yuji Naka . Yasuhara est resté avec Sega jusqu'à la fin des années 1990. Il a ensuite rejoint Naughty Dog , travaillant sur la série Jak and Daxter et Uncharted: Drake's Fortune , collaborant à nouveau avec l'ancien employé de Sega , Mark Cerny . Il était le directeur principal du design chez Namco Bandai Games America. En avril 2012, Yasuhara a rejoint Nintendo où il a accepté un poste au sein de la division Nintendo Software Technology . En 2016, il a rejoint Unity Technologies .</td>

